num1 = int(input("Enter first number:"))
num2 = int(input("Enter second number:"))
operator = input("Enter operator(+,-,*,/):")

if operator == ("+"):
    print("Resuilt",num1 + num2)
elif operator == ("-"):
    print("Result",num1 - num2)
elif operator == ("*"):
    print("Ressult",num1 * num2)
elif operator == ("/"):
    print("Result",num1 / num2)